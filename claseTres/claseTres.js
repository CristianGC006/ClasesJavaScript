/*Funciones declarativas*/

function funcionDeclarativa(){
    let nombre='Gustavo Petro';
    let edad=59
    console.log("Soy "+nombre+' Tengo '+edad+" Esta es una funcion declarativa")
}
funcionDeclarativa()

/*Funcion por expresion */
let funcionDeExpresion=function(){
    let nombre='Gustavo Petro';
    let edad=59
    console.log("Soy "+nombre+' Tengo '+edad +" Y Estoy aprendiendo funciones"+" Esto es una funcion de expresion")
}
funcionDeExpresion()